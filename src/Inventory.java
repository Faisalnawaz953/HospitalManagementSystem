    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String drcode=dcode.getText();
        try {
            pst=con.prepareStatement("Select * from item where id=?");
            pst.setString(1,drcode);
            rs=pst.executeQuery();
            while(rs.next()){
                int cqty,sprice,qty;
                cqty=rs.getInt("quan");
                sprice=rs.getInt("sprice");
                qty=Integer.parseInt(dquan.getValue().toString());
                int tot=sprice*qty;
                
                if(qty>cqty){
                    JOptionPane.showMessageDialog(this, "Quantity Short");
                }else{
                    
                    DefaultTableModel DF=(DefaultTableModel)jTable1.getModel();
                    DF.addRow(new Object[]{
                        prenoo.getText(),
                        dcode.getText(),
                        drname.getText(),
                   
                        dquan.getValue().toString(),
                          sprice,
                        tot,
                    
                });
                    int sum=0;
                    for(int i=0;i<jTable1.getRowCount();i++){
                        sum=sum+Integer.parseInt(jTable1.getValueAt(i,5).toString());
                        
                    }
                    
                    tcost.setText(Integer.toString(sum));
                    dcode.setText("");
                    drname.setText("");
                    dquan.setValue(0);
                    dcode.requestFocus();
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(Inventory.class.getName()).log(Level.SEVERE, null, ex);
        }
                 
    }//GEN-LAST:event_jButton2ActionPerformed

    private void dcodeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_dcodeKeyPressed
        // TODO add your handling code here:
         if(evt.getKeyCode()==KeyEvent.VK_ENTER){
             String drcode=dcode.getText();
             
             try {
                 pst=con.prepareStatement("Select * from item where id=?");
                 pst.setString(1,drcode);
                 rs=pst.executeQuery();
                 if(rs.next()==false){
                     JOptionPane.showMessageDialog(this,"Drug not exist");
                 }else{
                     String dname=rs.getString("name");
                     drname.setText(dname.trim());
                     dquan.requestFocus();
                 }
             } catch (SQLException ex) {
                 Logger.getLogger(Inventory.class.getName()).log(Level.SEVERE, null, ex);
             }
             
         }
    }//GEN-LAST:event_dcodeKeyPressed

