    public void sales(){
        DateTimeFormatter daa=DateTimeFormatter.ofPattern("yyyy/MM/dd");
        LocalDateTime now=LocalDateTime.now();
        String date=daa.format(now);
        String subtotal=tcost.getText();
        String paye=pay.getText();
        String bal=balance.getText();
        int lastinsert=0;
        
        
        try {
             String query="insert into sales(date,subtotal,pay,balance) Values(?,?,?,?)";
            pst=con.prepareStatement(query,Statement.RETURN_GENERATED_KEYS);
            pst.setString(1, date);
            pst.setString(2, subtotal);
            pst.setString(3, paye);
            pst.setString(4, bal);
            
            pst.executeUpdate();
            rs=pst.getGeneratedKeys();
            if(rs.next()){
                lastinsert=rs.getInt(1);
            }
            int rows=jTable1.getColumnCount();
            String query1="insert into sale_product(salesid,proid,sprice,qty,total) values(?,?,?,?,?)";
            pst=con.prepareStatement(query1);
            
            String presid,itemid,qty;
            int total,price;
            for(int i=0;i<jTable1.getRowCount();i++){
                presid=(String)jTable1.getValueAt(i,0);
                itemid=(String)jTable1.getValueAt(i,1);
                price=(int)jTable1.getValueAt(i,4);
                qty=jTable1.getValueAt(i,3).toString();
                int qty1=Integer.parseInt(qty);
                total=(int)jTable1.getValueAt(i,5);
                
                pst.setInt(1,lastinsert);
                pst.setString(2, itemid);
                
                pst.setInt(3,price);
                pst.setInt(4,qty1);
                pst.setInt(5,total);
                pst.executeUpdate();
                
                JOptionPane.showMessageDialog(this, "Data Inserted");
                
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(Inventory.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }

